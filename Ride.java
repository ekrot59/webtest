package jspbook.solProject;

public class Ride {
	private int r_id;
	private String r_name;
	private String r_place;
	private String r_offseason;
	private int r_price;
	private int count;
	
	
	public int getR_id() {
		return r_id;
	}
	public void setR_id(int r_id) {
		this.r_id = r_id;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	public String getR_place() {
		return r_place;
	}
	public void setR_place(String r_place) {
		this.r_place = r_place;
	}
	public String getR_offseason() {
		return r_offseason;
	}
	public void setR_offseason(String r_offseason) {
		this.r_offseason = r_offseason;
	}
	public int getR_price() {
		return r_price;
	}
	public void setR_price(int r_price) {
		this.r_price = r_price;
	}	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
